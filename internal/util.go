package internal

import (
	"cmp"
	"sort"
)

// OrderedMapKeys returns the keys of the map m in sorted order.
func OrderedMapKeys[M ~map[K]V, K cmp.Ordered, V any](m M) []K {
	r := make([]K, 0, len(m))
	for k := range m {
		r = append(r, k)
	}
	sort.Slice(r, func(i, j int) bool {
		return r[i] < r[j]
	})
	return r
}
